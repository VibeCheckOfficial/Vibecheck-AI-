export interface MarkdownOptions {
  /** Report title */
  title?: string;
  /** Include summary table */
  includeSummary?: boolean;
  /** Include file sections */
  includeFiles?: boolean;
  /** Include suggestions */
  includeSuggestions?: boolean;
  /** Use collapsible sections for findings */
  collapsible?: boolean;
  /** Maximum findings to show per file */
  maxFindingsPerFile?: number;
  /** Maximum total findings to show */
  maxFindings?: number;
  /** Include footer */
  includeFooter?: boolean;
  /** Footer text */
  footerText?: string;
  /** Use emoji icons */
  useEmoji?: boolean;
  /** Link to full report */
  reportLink?: string;
}

